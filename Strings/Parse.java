public class Parse {
    public static void main(String[] args) {
        //String to integer
        String str = "12345";
        int number = Integer.parseInt(str);
        System.out.println(number);

        //ToString method to string class
        int number1=123;
        String str1 = Integer.toString(number1);
        System.out.println(str1.length());
    }
}
